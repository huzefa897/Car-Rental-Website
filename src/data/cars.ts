// Get all unique brands
export const getAllBrands = (): string[] => {
  return [...new Set(cars.map(car => car.brand))].sort();
};

// Get all unique car types
export const getAllTypes = (): string[] => {
  return [...new Set(cars.map(car => car.type))].sort();
};

// Get all keywords for search suggestions
export const getAllKeywords = (): string[] => {
  const brands = getAllBrands();
  const types = getAllTypes();
  const models = [...new Set(cars.map(car => car.model))];
  
  // Extract words from descriptions
  const descriptionWords = cars
    .flatMap(car => car.description.split(' '))
    .filter(word => word.length > 3)
    .map(word => word.toLowerCase())
    .filter(word => !['with', 'and', 'the'].includes(word));
  
  return [...brands, ...types, ...models, ...descriptionWords];
};

// Get suggestions based on input
export const getSuggestions = (input: string): string[] => {
  if (!input.trim()) return [];
  
  const keywords = getAllKeywords();
  const lowerInput = input.toLowerCase();
  
  return keywords
    .filter(keyword => keyword.toLowerCase().includes(lowerInput))
    .slice(0, 5); // Limit to 5 suggestions
};

// Search cars by keyword
export const searchCars = (
  keyword: string,
  typeFilter: string | null,
  brandFilter: string | null
): Car[] => {
  if (!keyword && !typeFilter && !brandFilter) return cars;

  return cars.filter(car => {
    // Apply type filter if selected
    if (typeFilter && car.type !== typeFilter) return false;
    
    // Apply brand filter if selected
    if (brandFilter && car.brand !== brandFilter) return false;
    
    // If no keyword, apply only filters
    if (!keyword) return true;
    
    // Search by keyword
    const lowerKeyword = keyword.toLowerCase();
    return (
      car.brand.toLowerCase().includes(lowerKeyword) ||
      car.model.toLowerCase().includes(lowerKeyword) ||
      car.type.toLowerCase().includes(lowerKeyword) ||
      car.description.toLowerCase().includes(lowerKeyword)
    );
  });
};